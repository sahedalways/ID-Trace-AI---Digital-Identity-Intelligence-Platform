    $insert = $pdo->prepare("INSERT INTO `reports` (`vid`, `uid`, `status`, `created_at`) VALUES (?, ?, 'pending', NOW())");
    $insert->execute([$vid, isset($_SESSION['user_id']) ? (int)$_SESSION['user_id'] : null]);
    
    $reportStmt->execute([$vid]);
    $report = $reportStmt->fetch(PDO::FETCH_ASSOC);
}

$status = $report['status'] ?? 'pending';

// =========================================================================
// ASYNCHRONOUS ORCHESTRATION GATE: TRIGGER BRAIN WORKER IF PENDING
// =========================================================================
if ($status === 'pending' || isset($_GET['force_rebuild'])) {
    $pdo->prepare("UPDATE `reports` SET `status` = 'processing', `updated_at` = NOW() WHERE `vid` = ?")->execute([$vid]);
    

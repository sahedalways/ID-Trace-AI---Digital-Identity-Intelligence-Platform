// DYNAMIC ACCRUED VALIDITY TIME HORIZON MATRIX COMPUTATION
// =========================================================================
$daysRemaining = 30; 
if (!empty($report['created_at'])) {
    $creationTimestamp = strtotime($report['created_at']);
    $currentTimestamp  = time();
    $elapsedSeconds    = $currentTimestamp - $creationTimestamp;
    $elapsedDays       = floor($elapsedSeconds / (60 * 60 * 24));
    $daysRemaining     = 30 - $elapsedDays;
    if ($daysRemaining < 0) { $daysRemaining = 0; }
}

function escapeHtml($str) {
    return htmlspecialchars($str ?? '', ENT_QUOTES, 'UTF-8');
}

// Pull data strictly and solely out of the social_urls tracking sequence maps
$socialLinks = [];
if (!empty($target['social_urls'])) {
    foreach (explode(',', $target['social_urls']) as $part) {
        $trimmed = trim($part);
        if (!empty($trimmed) && !in_array($trimmed, $socialLinks)) { 
            $socialLinks[] = $trimmed; 
        }
    }
}

function getSocialIconClass($url) {
    $lower = strtolower($url);
    if (strpos($lower, 'facebook.com') !== false || strpos($lower, 'fb.com') !== false) return 'fa-brands fa-facebook text-blue-600 text-lg';
    if (strpos($lower, 'instagram.com') !== false) return 'fa-brands fa-instagram text-pink-600 text-lg';
    if (strpos($lower, 'linkedin.com') !== false) return 'fa-brands fa-linkedin text-blue-700 text-lg';
    if (strpos($lower, 'twitter.com') !== false || strpos($lower, 'x.com') !== false) return 'fa-brands fa-x-twitter text-slate-900 text-lg';
    if (strpos($lower, 'tiktok.com') !== false) return 'fa-brands fa-tiktok text-slate-950 text-lg';
    return 'fa-solid fa-link text-slate-400 text-lg';
}

